let nomes = ["Eduarda", "Lohan", "Pedro"];

console.log(nomes); //['Eduarda', 'Lohan', 'Pedro']

let elementoRemovido = nomes.pop();

console.log(elementoRemovido); // Pedro
console.log(nomes); //['Eduarda', 'Lohan']

nomes.push("Vitor");

console.log(nomes); //['Eduarda', 'Lohan', 'Vitor']

// pop = remove o elemento do fim do array
// push = adiciona um elemento no fim do array
