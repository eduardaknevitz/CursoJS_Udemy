// função que receba dois números: o primeiro é a base e o segundo a potência;
// Depois fazer essa operação e retorne o resultado;

function potencia(base, exp) {
  return Math.pow(base, exp); //OU return base ** exp;
}
console.log(potencia(2, 5));
console.log(potencia(2, 3));
console.log(potencia(3, 3));
